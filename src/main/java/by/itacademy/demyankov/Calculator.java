package by.itacademy.demyankov;

public class Calculator {

    public double add(double a, double b) {
        return a + b;
    }

    public int subtraction(int a, int b) {
        return a - b;
    }

    public int multiplay(int a, int b) {
        return a * b;
    }

    public int dividing(int a, int b) {
        return a / b;
    }

}

